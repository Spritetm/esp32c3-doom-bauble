const unsigned char *doom_iwad;
const unsigned int doom_iwad_len=4972760;

void I_Init(void)
{
    I_InitSound();
}

static void PrintVer(void)
{
    char vbuf[24];
    lprintf(LO_INFO,"%s",I_GetVersionString(vbuf,200));
}

void main() {
	doom_iwad=calloc(8*1024*1024, 1);
	FILE *f=fopen("../../doom1gba.wad", "r");
	if (!f) {
		printf("Can't open wad\n");
		exit(0);
	}
	int i=fread(doom_iwad, 1, 8*1024*1024, f);
	printf("Doom wad: %d bytes\n", i);
	fclose(f);


    /* cphipps - call to video specific startup code */
    I_PreInitGraphics();

    PrintVer();

    Z_Init();                  /* 1/18/98 killough: start up memory stuff first */

    InitGlobals();

    D_DoomMain ();
}
